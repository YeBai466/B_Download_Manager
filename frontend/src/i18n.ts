// Lightweight i18n: a module-level current language plus a subscription hook so
// React trees re-render when the language changes. Strings are looked up by key
// from per-language dictionaries; missing keys fall back to Chinese, then the key.
import { useEffect, useReducer } from "react";

export type Lang = "zh" | "en";

let currentLang: Lang = "zh";
const listeners = new Set<() => void>();

export function getLang(): Lang {
  return currentLang;
}

export function setLang(l: string | undefined | null): void {
  const next: Lang = l === "en" ? "en" : "zh";
  if (next === currentLang) return;
  currentLang = next;
  listeners.forEach((fn) => fn());
}

type Vars = Record<string, string | number>;

export function t(key: string, vars?: Vars): string {
  const table = dict[currentLang] ?? dict.zh;
  let s = table[key] ?? dict.zh[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}

// useLang subscribes a component to language changes (use it in window roots so
// the whole tree re-renders). Returns the active language.
export function useLang(): Lang {
  const [, force] = useReducer((c: number) => c + 1, 0);
  useEffect(() => {
    const fn = () => force();
    listeners.add(fn);
    return () => {
      listeners.delete(fn);
    };
  }, []);
  return currentLang;
}

